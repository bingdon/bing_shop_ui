package com.bing.goodsclass;

public class AttStoreInfo extends CarGoods{

	//店铺名称
	private String attStorename;
	//店铺信誉
	private String attStorelevel;
	//店铺图片地址
	private String attStoreheadurl;
	//店铺状态
	private String attStorestata;
	public String getAttStorename() {
		return attStorename;
	}
	public void setAttStorename(String attStorename) {
		this.attStorename = attStorename;
	}
	public String getAttStorelevel() {
		return attStorelevel;
	}
	public void setAttStorelevel(String attStorelevel) {
		this.attStorelevel = attStorelevel;
	}
	public String getAttStoreheadurl() {
		return attStoreheadurl;
	}
	public void setAttStoreheadurl(String attStoreheadurl) {
		this.attStoreheadurl = attStoreheadurl;
	}
	public String getAttStorestata() {
		return attStorestata;
	}
	public void setAttStorestata(String attStorestata) {
		this.attStorestata = attStorestata;
	}
	
	
	
	
}
